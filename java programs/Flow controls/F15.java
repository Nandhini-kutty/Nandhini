import java.util.*;
public class F15{
   public static void main(String[] args){
       int s=Integer.parseInt(args[0]);
       for(int i=1;i<=s;i++){
         for(int j=1;j<=i;j++){
              System.out.print("*");
              System.out.print(" ");
        }
        System.out.println(" ");
       }
       
}
}
